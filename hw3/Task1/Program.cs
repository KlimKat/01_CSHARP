// Задайте одномерный массив из 10 целых чисел от 1 до 100. 
// Найдите количество элементов массива, 
// значения которых лежат в отрезке [20,90].
// Пример
//[10 21 14 93 23] => 2

int[] arr = new int[10] { 50, 30, 80, 4, 54, 2, 7, 8, 10, 2 };
int count = 0;

    for (int i = 0; i < arr.Length; i++)
    {
        if (arr[i] >= 20 && arr[i] <= 90) 
        {
            count++;
        }
    }
     Console.WriteLine($"{count} ");